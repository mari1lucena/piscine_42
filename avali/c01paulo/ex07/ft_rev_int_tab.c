void	ft_swap(int *a, int *b)
{
	int	temp;

	temp = *a;
	*a = *b;
	*b = temp;
}

void	ft_rev_int_tab(int *tab, int size)
{
	int	middle;

	middle = (size / 2);
	while (--middle >= 0)
		ft_swap(&tab[middle], &tab[size - middle - 1]);
}
