#include <stdio.h>

int	ft_atoi(char *str)
{
	int	i;
	int	pn;
	int	num;

	i = 0;
	num = 0;
	pn = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			pn = pn * -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		num = (num * 10) + (str[i] - '0');
		i++;
	}
	return (num * pn);
}

int	main(void)
{
	char a[] = "     ---+--+1234ab567";
	printf("%d\n",ft_atoi(a));
}
