#include <unistd.h>

void	ft_print_comb2(void)
{
	int	p;
	int	s;

	p = 0;
	s = 1;
	while (p < 99)
	{
		while (s < 100)
		{
			write(1, &"0123456789"[p / 10], 1);
			write(1, &"0123456789"[p % 10], 1);
			write(1, " ", 1);
			write(1, &"0123456789"[s / 10], 1);
			write(1, &"0123456789"[s % 10], 1);
			if (!(p == 98 && s == 99))
			{
				write(1, ", ", 2);
			}
			s++;
		}
		p++;
		s = p + 1;
	}
}

int main(void)
{
	ft_print_comb2();
	return	0;
}
