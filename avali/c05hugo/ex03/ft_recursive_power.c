#include <stdio.h>

int	ft_recursive_power(int nb, int power)
{
	int	result;

	result = nb;
	if (power > 1)
		return (nb * ft_recursive_power(nb, (power - 1)));
	if (power == 0)
		return (1);
	if (power < 0)
		return (0);
	return (result);
}

int	main()
{
	int	nb;
	int	power;

	nb = 2;
	power = 7;
	printf("%d power by %d = %d\n", nb, power, ft_recursive_power(nb, power));
}

