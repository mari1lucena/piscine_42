#include <stdio.h>

int	ft_iterative_power(int nb, int power)
{
	int	result;

	result = nb;
	while (power > 1)
	{
		result *= nb;
		power--;
	}
	if (power == 0)
		return (1);
	if (power < 0)
		return (0);
	return (result);
}

int	main()
{
	int	nb;
	int	power;

	nb = 2;
	power = 0;
	printf("%d power by %d = %d", nb, power, ft_iterative_power(nb, power));
}

