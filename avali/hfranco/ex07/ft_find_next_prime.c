#include <stdio.h>

int	ft_is_prime(int nb)
{
	int	i;

	i = 2;
	if (nb <= 1)
		return (0);
	while (i < nb)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}

int	ft_find_next_prime(int nb)
{
	while (!(ft_is_prime(nb)))
		nb++;
	return (nb);
}

int main(void)
{
	int num = 53;
	int next_cousin = ft_find_next_prime(num);
	printf("o proximo primo depois de %d Ã© %d\n", num, next_cousin);
	return (0);
}
