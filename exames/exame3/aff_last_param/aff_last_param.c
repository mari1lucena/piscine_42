#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;

	i = 0;
	if (ac < 2)
		write(1, "\n", 1);
	if (ac != 2)
	{
		while(av[ac - 1][i] != '\0')
		{
			write(1, &av[ac -1][i], 1);
			i++;
		}
	}
}