#include <unistd.h>
int	main(int ac, char **av)
{
	int	i = 0;
	int	x = 0;
	int	z = 0;

	if(ac == 2)
	{
		while(av[1][i])
		{
			if(av[1][i] >= 'a' && av[1][i] <= 'z')
			{
				x = av[1][i] - 96;
			}
			if(av[1][i] >= 'A' && av[1][i] <= 'Z')
				x = av[1][i] - 64;
			while (z < x)
			{
				write(1, &av[1][i], 1);
				z++;
			}
			z = 0;
			i++;
		}
	}
}